void MazeGenerator()
{
    srand(time(nullptr));

    constexpr auto maze_size_x = 100;
    constexpr auto maze_size_y = 100;
    std::vector<std::vector<bool>> maze;
    std::list<std::pair<int, int>> drillers;

    maze.resize(maze_size_y);
    for (size_t y = 0; y < maze_size_y; ++y)
        maze[y].resize(maze_size_x);

    for (size_t y = 0; y < maze_size_y; ++y)
        for (size_t x = 0; x < maze_size_x; ++x)
            maze[y][x] = false;

    drillers.push_back(std::make_pair(maze_size_x / 2, maze_size_y / 2));
    while (drillers.size() > 0)
    {
        std::list<std::pair<int, int>>::iterator m;
        std::list<std::pair<int, int>>::iterator _m;
        m = drillers.begin();
        _m = drillers.end();
        while (m != _m)
        {
            auto remove_driller = false;
            switch (rand() % 4)
            {
                case 0:
                    (*m).second -= 2;
                    if ((*m).second<0 || maze[(*m).second][(*m).first])
                    {
                        remove_driller = true;
                        break;
                    }
                    maze[(*m).second + 1][(*m).first] = true;
                    break;
                case 1:
                    (*m).second += 2;
                    if ((*m).second >= maze_size_y || maze[(*m).second][(*m).first])
                    {
                        remove_driller = true;
                        break;
                    }
                    maze[(*m).second - 1][(*m).first] = true;
                    break;
                case 2:
                    (*m).first -= 2;
                    if ((*m).first<0 || maze[(*m).second][(*m).first])
                    {
                        remove_driller = true;
                        break;
                    }
                    maze[(*m).second][(*m).first + 1] = true;
                    break;
                case 3:
                    (*m).first += 2;
                    if ((*m).first >= maze_size_x || maze[(*m).second][(*m).first])
                    {
                        remove_driller = true;
                        break;
                    }
                    maze[(*m).second][(*m).first - 1] = true;
                    break;
            }
            if (remove_driller)
                m = drillers.erase(m);
            else
            {
                drillers.push_back(std::make_pair((*m).first, (*m).second));
                // uncomment the line below to make the maze easier
                // if (rand()%2)
                drillers.push_back(std::make_pair((*m).first, (*m).second));

                maze[(*m).second][(*m).first] = true;
                ++m;
            }
        }
    }

    auto map = new unsigned char[maze_size_x * maze_size_y];
    for (size_t y = 0; y < maze_size_y; ++y)
        for (size_t x = 0; x < maze_size_x; ++x)
            map[y * maze_size_x + x] = maze[y][x];

    auto startX = 0;
    auto startY = 0;
    auto targetX = 0;
    auto targetY = 0;
    for (size_t y = 0; y < maze_size_y; ++y)
        for (size_t x = 0; x < maze_size_x; ++x)
            if (maze[y][x])
            {
                startX = x;
                startY = y;

                y = maze_size_y;
                break;
            }
    for (auto y = maze_size_y - 1; y >= 0; --y)
        for (auto x = maze_size_x - 1; x >= 0; --x)
            if (maze[y][x])
            {
                targetX = x;
                targetY = y;
                y = -1;
                break;
            }

    int pOutBuffer[1024];
    for (auto i = 0; i < TEST_SIZE; ++i)
        FindPath(startX, startY, targetX, targetY, map, maze_size_x, maze_size_y, pOutBuffer, 1024);
    delete[] map;
}
